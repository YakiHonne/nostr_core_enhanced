// Regex patterns
final RegExp BUNKER_REGEX =
    RegExp(r'^bunker://([0-9a-f]{64})\??([?\\/\w:.=&%-]*)$');
final RegExp NIP05_REGEX =
    RegExp(r'^([a-z0-9._-]+)@([a-z0-9.-]+\.[a-z]{2,})$', caseSensitive: false);
final RegExp EMAIL_REGEX = RegExp(r'^[^\s@]+@[^\s@]+\.[^\s@]+$');

