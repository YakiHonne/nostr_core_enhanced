enum RemoteSignerState {
  disconnected,
  connecting,
  awaitingAuth,
  connected,
  failed,
}

class BunkerPointer {
  final List<String> relays;
  final String pubkey;
  final String? secret;

  BunkerPointer({
    required this.relays,
    required this.pubkey,
    this.secret,
  });

  String toBunkerURL() {
    final uri = Uri(
      scheme: 'bunker',
      host: pubkey,
      queryParameters: {
        if (secret != null) 'secret': secret!,
        ...relays.asMap().map((_, relay) => MapEntry('relay', relay)),
      },
    );
    return uri.toString();
  }
}

